// Profile Music Player
(function() {
    // Configuration - Replace this YouTube video ID with your chosen song
    // Current song: https://www.youtube.com/watch?v=odeHP8N4LKc
    // To get the video ID: from https://www.youtube.com/watch?v=ABC123XYZ, use 'ABC123XYZ'
    const YOUTUBE_VIDEO_ID = 'odeHP8N4LKc'; // Your chosen song
    const START_TIME = 329; // Start at 5 minutes 28 seconds (5*60 + 28 = 328 seconds)
    
    let isPlaying = false;
    let player = null;
    let playerReady = false;
    let hasStartedPlaying = false; // Track if we've started playing at least once (for pause/resume functionality)
    let isMobile = /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent);
    
    // Load YouTube IFrame API
    function loadYouTubeAPI() {
        if (window.YT) {
            initPlayer();
            return;
        }
        
        const tag = document.createElement('script');
        tag.src = 'https://www.youtube.com/iframe_api';
        const firstScriptTag = document.getElementsByTagName('script')[0];
        firstScriptTag.parentNode.insertBefore(tag, firstScriptTag);
        
        // YouTube API will call this when ready
        window.onYouTubeIframeAPIReady = initPlayer;
    }
    
    function initPlayer() {
        // Create hidden div for YouTube player
        const playerDiv = document.createElement('div');
        playerDiv.id = 'youtube-player';
        playerDiv.style.display = 'none';
        document.body.appendChild(playerDiv);
        
        player = new YT.Player('youtube-player', {
            height: '1',
            width: '1',
            videoId: YOUTUBE_VIDEO_ID,
            playerVars: {
                'autoplay': 0,
                'controls': 0,
                'disablekb': 1,
                'fs': 0,
                'modestbranding': 1,
                'rel': 0,
                'showinfo': 0,
                'preload': 'auto' // Preload the video
            },
            events: {
                'onReady': onPlayerReady,
                'onStateChange': onPlayerStateChange,
                'onError': onPlayerError
            }
        });
    }
    
    function onPlayerReady(event) {
        playerReady = true;
        player.setVolume(50); // Set volume to 50%
        
        // Preload the video by seeking to the start time (this loads audio data)
        player.seekTo(START_TIME, true);
        
        // Remove loading state and show ready state
        const playBtn = document.getElementById('musicPlayBtn');
        if (playBtn) {
            playBtn.classList.remove('loading');
            playBtn.title = 'Play my theme song';
        }
        
        console.log('YouTube player ready and preloaded at', START_TIME, 'seconds');
    }
    
    function onPlayerStateChange(event) {
        const playBtn = document.getElementById('musicPlayBtn');
        if (!playBtn) return;
        
        if (event.data === YT.PlayerState.PLAYING) {
            isPlaying = true;
            playBtn.classList.add('playing');
            playBtn.title = 'Pause music';
        } else if (event.data === YT.PlayerState.PAUSED) {
            isPlaying = false;
            playBtn.classList.remove('playing');
            playBtn.title = 'Play my theme song';
        } else if (event.data === YT.PlayerState.ENDED) {
            isPlaying = false;
            playBtn.classList.remove('playing');
            playBtn.title = 'Play my theme song';
            // Reset to start time when song ends and reset the flag
            player.seekTo(START_TIME, true);
            hasStartedPlaying = false; // Reset flag so next play starts from beginning again
        }
    }
    
    function onPlayerError(event) {
        console.error('YouTube player error:', event.data);
        const playBtn = document.getElementById('musicPlayBtn');
        if (playBtn) {
            playBtn.title = 'Error loading song';
            isPlaying = false;
            playBtn.classList.remove('playing');
        }
    }
    
    // Initialize the music player
    function initMusicPlayer() {
        const playBtn = document.getElementById('musicPlayBtn');
        if (!playBtn) return;
        
        // Skip initialization on mobile devices
        if (isMobile) {
            console.log('Mobile device detected - music player disabled');
            return;
        }
        
        playBtn.addEventListener('click', toggleMusic);
        
        // Show loading state initially for desktop
        playBtn.classList.add('loading');
        playBtn.title = 'Loading song...';
        
        // Start loading YouTube API immediately
        loadYouTubeAPI();
    }
    
    function toggleMusic() {
        const playBtn = document.getElementById('musicPlayBtn');
        
        if (!playerReady || !player) {
            playBtn.title = 'Song still loading...';
            console.log('Player not ready yet...');
            return;
        }
        
        if (!isPlaying) {
            try {
                // Only seek to start time if this is the first play or if we've reset
                if (!hasStartedPlaying) {
                    player.seekTo(START_TIME, true);
                    hasStartedPlaying = true;
                }
                player.playVideo();
            } catch (error) {
                console.error('Error playing video:', error);
                playBtn.title = 'Error playing song';
            }
        } else {
            player.pauseVideo();
        }
    }
    
    // Initialize when DOM is ready
    document.addEventListener('DOMContentLoaded', function() {
        // Small delay to ensure everything is rendered
        setTimeout(initMusicPlayer, 500);
    });
})();
